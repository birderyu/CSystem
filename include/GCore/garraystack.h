#ifndef _CORE_ARRAY_STACK_H_
#define _CORE_ARRAY_STACK_H_

#include "gstack.h"
#include "gdynamicarray.h"

namespace gsystem { // gsystem

/********************************************************************************
**
** @brief 使用数组实现的栈
** @template {DataT} 栈元素的类型
**
********************************************************************************/
template<typename DataT>
class GArrayStack 
	: public GStack<DataT>
{
public:
	/****************************************************************************
	**
	** @name GArrayStack
	** @brief 构造函数（constructor）
	** @param[in] capacity {gsize} 预分配栈元素的数量，默认为0
	**
	** 构造一个默认的栈，并为其预分配capacity个元素的空间
	**
	****************************************************************************/
	GArrayStack(gsize capacity = 0);

	/****************************************************************************
	**
	** @name GArrayStack
	** @brief 拷贝构造函数（copy constructor）
	** @param[in] stack {const GArrayStack<DataT> &} 栈
	**
	****************************************************************************/
	GArrayStack(const GArrayStack<DataT> &stack);

	/****************************************************************************
	**
	** @name GArrayStack
	** @brief 移动构造函数（move constructor）
	** @param[in] stack {GArrayStack<DataT> &&} 栈
	**
	****************************************************************************/
	GArrayStack(GArrayStack<DataT> &&stack);

	/****************************************************************************
	**
	** @name operator=
	** @brief 拷贝运算符
	** @param[in] stack {const GArrayStack<DataT> &} 栈
	** @return {GArrayStack<DataT> &} 栈的引用
	**
	****************************************************************************/
	GArrayStack<DataT>& operator=(const GArrayStack<DataT> &stack);

	/****************************************************************************
	**
	** @name operator=
	** @brief 移动运算符
	** @param[in] stack {GArrayStack<DataT> &&} 栈
	** @return {GArrayStack<DataT> &} 栈的引用
	**
	****************************************************************************/
	GArrayStack<DataT>& operator=(GArrayStack<DataT> &&stack);

	/****************************************************************************
	**
	** @name Size
	** @brief 获取栈元素的数量
	** @return {gsize} 元素的数量
	** @see GStack
	**
	****************************************************************************/
	gsize Size() const;

	/****************************************************************************
	**
	** @name IsEmpty
	** @brief 判断栈是否为空
	** @return {gbool} 若元素的数量为0，则返回true，否则返回false
	** @see GStack
	**
	****************************************************************************/
	gbool IsEmpty() const;

	/****************************************************************************
	**
	** @name Destroy
	** @brief 销毁栈
	** @see GStack
	**
	** 清空栈的数组，并且释放内存空间
	**
	****************************************************************************/
	gvoid Destroy();

	/****************************************************************************
	**
	** @name Clear
	** @brief 清空栈
	** @see GStack
	**
	** 仅清空栈的数组，不释放内存空间
	**
	****************************************************************************/
	gvoid Clear();
	
	/****************************************************************************
	**
	** @name Push
	** @brief 入栈
	** @param [in] value {const DataT &} 值
	** @see GStack
	**
	****************************************************************************/
	gvoid Push(const DataT &data);

	/****************************************************************************
	**
	** @name Push
	** @brief 入栈
	** @param [in] value {DataT &&} 值
	** @see GStack
	**
	****************************************************************************/
	gvoid Push(DataT &&data);

	/****************************************************************************
	**
	** @name Pop
	** @brief 出栈
	** @param [out] value {DataT *} 用于接受出队的值
	** @see GStack
	**
	****************************************************************************/
	gvoid Pop(DataT *data = GNULL);

	/****************************************************************************
	**
	** @name Head
	** @brief 获取栈顶元素
	** @return {const DataT &} 栈顶元素的引用
	** @see GStack
	**
	****************************************************************************/
	const DataT &Top() const;

	/****************************************************************************
	**
	** @name Head
	** @brief 获取栈顶元素
	** @return {DataT &} 栈顶元素的引用
	** @see GStack
	**
	****************************************************************************/
	DataT &Top();

private:
	/****************************************************************************
	**
	** @name Capacity
	** @brief 获取容量（预分配内存）
	** @return {gsize} 预分配元素的数量
	**
	****************************************************************************/
	gsize Capacity() const;

	/****************************************************************************
	**
	** @name Reserve
	** @brief 重新预分配内存
	** @param [in] capacity {gsize} 预分配元素的数量
	**
	****************************************************************************/
	gvoid Reserve(gsize capacity);

	/****************************************************************************
	**
	** @name Fill
	** @brief 判断栈的内存是否被填满
	** @return {gbool} 若栈的内存已经被填满，则返回true，否则返回false
	**
	****************************************************************************/
	gbool Fill() const;

private:
	/****************************************************************************
	**
	** 栈顶
	**
	****************************************************************************/
	gsize m_TopCursor;

	/****************************************************************************
	**
	** 数组
	**
	****************************************************************************/
	GDynamicArray<DataT> m_tArray;
};

} // namespace gsystem

#include "inline/garraystack.inl"

#endif // _CORE_ARRAY_STACK_H_
