#ifndef _CORE_STOP_WATCH_H_
#define _CORE_STOP_WATCH_H_

#include "gtype.h"

namespace gsystem { // gsystem

/********************************************************************************
**
** @brief 秒表
**
********************************************************************************/
class GAPI GStopWatch
{
public:
	/****************************************************************************
	**
	** @name GStopWatch
	** @brief 构造函数（constructor）
	**
	****************************************************************************/
	GStopWatch();

	/****************************************************************************
	**
	** @name Start
	** @brief 开始计时
	**
	****************************************************************************/
	gvoid Start();

	/****************************************************************************
	**
	** @name Tick
	** @brief 获取当前时刻距离开始计时时刻的毫秒数
	** @return {gulong} 毫秒数
	**
	****************************************************************************/
	gulong Tick() const;

	/****************************************************************************
	**
	** @name Stop
	** @brief 停止计时，并返回停止计时时刻距离开始计时时刻的毫秒数
	** @return {gulong} 毫秒数
	**
	****************************************************************************/
	gulong Stop();

private:
	/****************************************************************************
	**
	** @name Moment
	** @brief 获取当前时刻的毫秒数
	** @return {gulong} 毫秒数
	**
	****************************************************************************/
	gulong Moment() const;

private:
	/****************************************************************************
	**
	** 开始计时时的毫秒数
	**
	****************************************************************************/
	gulong m_nStart;
};

} // namespace gsystem

#endif // _CORE_STOP_WATCH_H_
